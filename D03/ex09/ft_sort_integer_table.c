void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int fab;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				fab = tab[i];
				tab[i] = tab[j];
				tab[j] = fab;
			}
			j++;
		}
		i++;
	}
}
